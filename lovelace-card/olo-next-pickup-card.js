function getEntityConfig(entity) {
  const attrs = entity?.attributes || {};
  return {
    wasteTypes: attrs.waste_types || [],
    dayLabel: attrs.day_label || "Bez dat",
    dateLabel: attrs.date_label || "Skontroluj add-on",
    isTomorrow: !!attrs.is_tomorrow,
    upcoming: Array.isArray(attrs.upcoming) ? attrs.upcoming : [],
  };
}

